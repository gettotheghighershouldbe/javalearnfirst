public class TestSwitch {
	public static void main (String[] args) {
		int i = 2;
		switch (i) 
		{
			case 1:
			System.out.println("A");
			break;
			case 2:
			System.out.println("B");
			break;
			case 3:
			System.out.println("C");
			break;
			case 4:
			System.out.println("D");
			break;
			default:
			System.out.println("error");
			break;


		}
	}
}